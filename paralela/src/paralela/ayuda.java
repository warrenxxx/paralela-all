package paralela;

/**
 *
 * @author warren
 */
public class ayuda {
    public double a;
    public double b;

    public ayuda() {
        a=0;
        b=0;
    }
    public void masa(){
        a++;
    }
    public void menosa(){
        a--;
    }
    public void masb(){
        b++;
    }
    public void menosb(){
        b--;
    }
}
